export function getCategoryForIngredient(name: string): string {
  const lower = name.toLowerCase()
  for (const cat of ingredientCategories) {
    if (cat.items.some((item) => lower.includes(item.en) || item.en.includes(lower))) {
      return cat.name.en
    }
  }
  return 'Other'
}

